public class Employee
{
	private int empId;
	private String empName;

		public void setempId(int id)
		{
			this.empId=id;
		}
		public void setempName(String name)
		{
			this.empName=name;
		}
		public int getId()
		{
			return this.empId;
		}
		public String getName()
		{
			return this.empName;
		}
}